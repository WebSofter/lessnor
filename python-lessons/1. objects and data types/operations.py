"""
+ - Add
""" 
x = 1
y = 2
z = x + y
print("Result of '+':", z)

"""
- - Add
""" 
x = 10
y = 2
z = x - y
print("Result of '-':", z)

"""
/ - Divide
""" 
x = 5
y = 2
z = x / y
print("Result of '/':", z)

"""
* - Mult
""" 
x = 5
y = 2
z = x * y
print("Result of '*':", z)

"""
// - Divide and return module of result
"""
x = 5
y = 2
z = x // y
print("Result of '//':", z)

"""
% - Remainder of the division
"""
x = 5
y = 2
z = x % y
print("Result of '%':", z)

"""
** - Exponentiation
"""
x = 5
y = 2
z = x ** y
print("Result of '**':", z)

"""
<, >, <=, >=, == - Return boolean value
"""
x = 5
y = 2
z = x > y
print("Result of '>':", z)
