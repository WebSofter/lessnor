"""
+ - Summ 2 and more strings
"""
x = "Hello "
y = "World!"
z = x + y
print("Result of sum %s + %s:"%(x, y), z)

"""
* - Mult string with value
"""
x = "Hello "
y = 3
z = x * y
print("Result of sum %s * %s:"%(x, y), z)

"""
\ - Enscape specchar in text
"""
x = 'I have\'t big car'
print("Result of \\ ", x)

"""
r - Convert as raw string
"""
x =r"C:\Program Files\Python3"
print("Result of r ", x)

"""
u - Convert as unicode string
"""
x =u"C:\Program Files\Python3"
print("Result of r ", x)